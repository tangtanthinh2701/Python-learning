# Comparison operators
print(20 == 20)
print("a" == "b")

# Not equal
print(20 != 10)
print("a" != "b")

# >, >=, <, <=
print(15 > 10)
print(15 <= 15)

# logical operators
num = 15
print("Number is in the range [10, 20]:", 15 <= num <= 20)
print("Number is out of the range [10, 20]:", num < 10 or num > 20)

bol = False
print(not bol)
print(not 6 > 5)
