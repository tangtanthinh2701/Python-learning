# Addition +
print(3 + 10)

# Subtraction -
print(3 - 10)

# Multiplication *
print(3 * 10)

# Division /
print(10 / 3)
print(int(10 / 3)) # int

# Modulus %
print(10 % 3)
print(-10 % 3)

# Exponentiation ** Lũy thừa
print(2 ** 4)
print(2 ** 4.5)

# Floor division // Nhận được số nguyên lớn nhất  kq<=
# N = D * (N // D) + (N % D)
print(10 // 3)
print(-10 // 3)

